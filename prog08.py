#entrar com dois números inteiro e exibir o maior
n1=int(input('Digite o primeiro número: '))
n2=int(input('Digite o segundo número: '))
if n1==n2:
    print('os números são iguais')
if n1>n2:
    print(f'o {n1} é maior')
if n2>n1:
    print(f'o {n2} é maior')
